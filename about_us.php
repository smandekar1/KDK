						<b>Product Description</b><br>Hand holds or grab irons allow mobility of personnel in and around railcars. Hand holds are produced from carbon steels and from stainless steels and can be manufactured in a variety of configurations. KDK has tooling for most standard configurations currently in the railcar industry. Adjustable hand holds are also available that allow parts to be used in an application where multiple lengths are required. One end with a series of holes passes through the mating part and a <a href="http://www.kdkforging.com/pins.php" title="Pins" style="color:#333333;">pin</a> is placed to secure the length. The excess portion of the bar can then be cut off. The mating portion of the hand hold can be manufactured by punching or for finer tolerances can be drilled. The shape of the attached foot area is dictated from the customer. Finish of the component is also dictated by the customer. The flat area of the hand hold is also known as a mounting flange and they are typically design to be flat or flush with the mounting surface. They are then typically riveted or screwed to the surface of the railcar. Standard in producing handholds can be found at the American Association of Railroads website (www.aar.org) website. Hand holds can be used for passenger and industrial railcars. KDK hand holds are typically made of .75"  or 1" dia.  steel and can vary in length from 4" to 15 feet in length.</p>
						
