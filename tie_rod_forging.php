						<b>Product Description</b><br>The <a href="http://www.kdkforging.com/ride_control.php" title="Tie Rod" style="color:#333333; text-decoration:underline;">Tie Rod</a> connects the center link to the steering knuckle on cars that have conventional suspensions. The Tie Rod connects to the end of the rack in rack and pinion steering systems. The tie rod transfers force from the steering center link or rack gear to the knuckle, causing the wheel to turn. The outer tie rod end connects with and sleeve, allowing the tie rod to be adjustable. This adjusts the vehicle's toe, an angle alignment measure. KDK manufactures Tie rods to manufacturers specifications for a variety of cars and other off highway type applications. The cup diameters of tie rods can range from ¾ inch to 3 ¼ inches. We can also provide rough <a href="http://www.kdkforging.com/machined_socket.php" title="Machined Sockets" style="color:#333333; text-decoration:underline;">machined sockets</a> as well as unfurnished tie rods for you to finish machine, grind, or assemble. Our tie rods come ready to be assembled with bearings or grease fittings. Tie Rods may have 1 or more bends in the forging. KDK is equipped to handle various bends located along the bar. KDK can make all tooling for the various bends in the forging as well as for the boss or diamond applications. We also offer coating and heat treating. Heat treating services offered include normalizing and hardening. The <a href="http://www.kdkforging.com/tie_rod_forging.php" title="Tie Rod Forging" style="color:#333333; text-decoration:underline;">upset forging</a> process adds strength to the steel because the grain flow structure is improved. KDK will make a custom die for your forging needs. Please send us a copy of your drawing and we will be happy to review your part and send a quote to you.</p>
						
