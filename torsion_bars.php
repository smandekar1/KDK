						<b>Product Description</b><br>Torsion bars are used to absorb the shock in the chassis or a vehicle.  They can be used in conjunction with other <a href="http://www.kdkforging.com/ride_control.php" title="Ride Control Components" style="color:#333333; text-decoration:underline;">ride control components</a> to provide a smoother ride.  KDK manufactures torsion bars in a variety of shapes and sizes for several different applications.  The diameters of the Torsion bars can range from 1 inch to 2 ½ inches.  The length of automotive torsion bars typically range from 3 feet to 5 feet.  Torsion bars are created to suit the head configuration as dictated by the customer.  Torsion bar forgings made by KDK can be in a round head configuration or in a hex configuration.  Typically, in the round head configuration, the end would then be machined and then they would have a spline machined into them.  The hex head configuration would not require any machining after the forging process and would be idea in certain applications.  Torsion bars can be coated or heat treated after the forging process depending upon the application.  KDK has several different head configurations for Torsion bars in stock.  The forging process adds strength to the Torsion bar allowing it to twist back and forth over the lifecycle of the vehicle.  One of the common problems that has come from the use of <a href="http://www.kdkforging.com" title="Forgings" style="color:#333333; text-decoration:underline;">forgings</a> has been the appearance of flash lines or parting lines.  KDK torsion bar forgings are made with minimal parting lines and can be eliminated completely.  Please send us a copy of your torsion bar drawing and we will be happy to review your part and send a quote to you.</p>
						
